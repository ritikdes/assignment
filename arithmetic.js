let a = 10, b = 5;

// Arithmetic Operators
let sum = a + b;
let product = a * b;

// Comparison Operators
let isEqual = (a == b);
let isGreater = (a > b);

// Logical Operators
let andResult = (a > b && b > 0); // AND operation
let orResult = (a > b || b < 0);  // OR operation

console.log(`Sum: ${sum}, Product: ${product}`);
console.log(`a == b: ${isEqual}, a > b: ${isGreater}`);
console.log(`AND Result: ${andResult}, OR Result: ${orResult}`);
